# built-ins do Python: __init__, len(), iter(), int(), etc

# Quando utilizamos o if, estamos criando um novo block de código. Sempre que isso for
# feito, devemos utilizar o caractere ':'
if __name__ == '__main__':
    print("Hello world!")

    # podemos ter mais de 1 statement por linha, apesar de não ser recomendado. Separamos
    # esses statements por ';'
    print("Hello 2"); print("Hello 3")


    # Python é uma linguagem case-sensitive, ou seja, diferencia maiúsculas e
    # minúsculas
    ano = 1900
    aNo = 2000
    print(ano, aNo)
